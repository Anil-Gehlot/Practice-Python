'''
sorted() method is used to sort the given string according to their ascii value.
'''

str1 = 'ABCDasdfdh'
str2 = 'absdf98r302'
str3 = 'djhf89SJC'
 
print(sorted(str1))     # ['A', 'B', 'C', 'D', 'a', 'd', 'd', 'f', 'h', 's']
print(sorted(str2))     # ['0', '2', '3', '8', '9', 'a', 'b', 'd', 'f', 'r', 's']
print(sorted(str3))     # ['8', '9', 'C', 'J', 'S', 'd', 'f', 'h', 'j']

