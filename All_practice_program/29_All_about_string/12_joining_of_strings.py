'''
We can join a group of strings(list or tuple) with respect to the given seperator.
a = seperator.join(group of strings)
'''

list = ['hello',"I'm","ANIL",'gehlot']
tup=('hyderabad','singapore','london','dubai')

print('-'.join(list))       # hello-I'm-ANIL-gehlot
print(":".join(tup))        # hyderabad:singapore:london:dubai

