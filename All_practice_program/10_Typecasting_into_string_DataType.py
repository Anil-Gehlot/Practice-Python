"""
Typecasting :- converting one datatype into another datatype is known as Typecasting or 
                Typeconversion or Type coersion.
                There are 5 built-in function in python for typecasting-->>
                1. int()
                2. float()
                3. complex()
                4. bool()
                5. str()
                """

